void ThermalCamera::logFrame(ThermalFrame &frame)
{

    LOG_INF("=== Frame %u | subpage %u | warmed: %d ===", frame.frameId, frame.subPage, (int)frame.warmedData);

    // Each pixel formatted as "XXX.X " = 6 chars, 24 cols per row
    char rowBuf[FRAME_COLS * 6 + 1];

    for (int row = 0; row < FRAME_ROWS; row++)
    {
        int offset = 0;
        for (int col = 0; col < FRAME_COLS; col++)
        {
            float t = frame.pixels[row * FRAME_COLS + col];
            int whole = (int)t;
            int frac = (int)((t - (float)whole) * 10);
            if (frac < 0)
            {
                frac = -frac;
            }
            offset += snprintf(rowBuf + offset, sizeof(rowBuf) - offset, "%3d.%d ", whole, frac);
        }
        LOG_INF("R%02d: %s", row, rowBuf);
    }
}

int ThermalCamera::close()
{

    if (!running_ && upFrameThreadPtr == nullptr)
    {

        LOG_DBG("Tried Ending process without initializing");
        return 0; // TODO: should this be an error code?
    }

    running_ = false;

    k_thread_abort(upFrameThreadPtr);

    k_thread_join(upFrameThreadPtr, K_FOREVER);
}



ThermalFrame* ThermalCamera::getUpdatedFrame(){


    int ret = k_sem_take(&frameReadySem_, K_NO_WAIT);
    if(ret != 0 ){
        return nullptr;
    }

    k_mutex_lock(&frameMutex_, K_FOREVER);
    ThermalFrame *safePtr = externFramePtr;
    k_mutex_unlock(&frameMutex_);


    return safePtr;

}